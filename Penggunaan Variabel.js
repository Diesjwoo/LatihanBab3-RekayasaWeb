 // Deklarasi variabel 
 let nama = "Fulan"; 
 const umur = 25; 
 var pekerjaan = "Programmer"; 
   
 // Menampilkan variabel 
 console.log(nama); // Output: Fulan 
 console.log(umur); // Output: 25 
 console.log(pekerjaan); // Output: Programmer 
   
 // Mengubah nilai variabel 'let' dan 'var' 
 nama = "Fulana"; 
 pekerjaan = "Designer"; 
   
 console.log(nama); // Output: Fulana 
 console.log(pekerjaan); // Output: Designer 