// Deklarasi variabel 
var nama = "Fulan"; 
let usia = 25;
const kota = "Klaten"; 
   
 // Menampilkan nilai awal 
 console.log("Nama:", nama);   // Output: Nama: Fulan 
 console.log("Usia:", usia);   // Output: Usia: 25 
 console.log("Kota:", kota);   // Output: Kota: klaten 
   
 // Mengubah nilai variabel var dan let 
 nama = "fulana"; 
 usia = 13; 
   
 // Menampilkan nilai setelah diubah 
 console.log("Nama baru:", nama); // Output: Nama baru: fulana 
 console.log("Usia baru:", usia); // Output: Usia baru: 13 
   
